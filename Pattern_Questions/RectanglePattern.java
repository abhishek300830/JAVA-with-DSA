package Pattern_Questions;
// creating the rectangle pattern from stars of width 5 and height 4.
public class RectanglePattern {
    public static void main(String[] args) {
        for(int i=0;i<4;i++){
            for(int j=0;j<5;j++){
                System.out.print("*");
            }
            System.out.println();

        }
            
    }
}
